    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String firstname = jTextField1.getText();
    String lastname = jTextField2.getText();
    String userid = jTextField3.getText();
    String email = jTextField4.getText();
    String phone = jTextField5.getText();
    String password = jPasswordField1.getText();
    String utype = jComboBox1.getSelectedItem().toString();
    try{
    String s = "INSERT INTO `signup`(`firstname`, `lastname`,`email`,`phone`, `userid`, `password`, `Usertype`) VALUES (?,?,?,?,?,?,?)";
    con = DriverManager.getConnection("jdbc:mysql://localhost:3306/signup","root","");
    pst = con.prepareStatement(s);
    pst.setString(1, firstname);
    pst.setString(2, lastname);
    pst.setString(3, email);
    pst.setString(4, phone);
    pst.setString(5, userid);
    pst.setString(6, password);
    pst.setString(7, utype);
    pst.executeUpdate();
    JOptionPane.showMessageDialog(null,"REGISTERED SUCCESSFULLY");
    
    }catch(Exception e1){
        JOptionPane.showMessageDialog(null,e1);
    }
    Login l = new Login();
    l.setVisible(true);
    dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

